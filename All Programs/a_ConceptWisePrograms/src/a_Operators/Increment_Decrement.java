package a_Operators;

public class Increment_Decrement {

	public static void main(String[] args) 
	{
		int a =5;
		System.out.println(a);
		System.out.println(a++);
		a+=5;
		System.out.println(a);
		a/=2;
		System.out.println(a);
		a++;
		System.out.println(a);
		--a;
		System.out.println(a);
		a--;
		System.out.println(a);
	}

}
