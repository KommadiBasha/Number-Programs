package FirstRoundPractiesAllPrograms;

public class Swaping {

	public static void main(String[] args) 
	{
		int num1=12;
		int num2=31;
		
		
		System.out.println("num1 "+num1);
		System.out.println("num2 "+num2);
		
		int store=num1;
		num1=num2;
		num2=store;
		System.out.println();
		
		System.out.println(num1+" num1 ");
		System.out.println(num2+" num2 ");

	}

}
